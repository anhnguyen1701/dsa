    private void loadSach() {
        File f = new File(fS);
        if (f.exists()) {
            this.listSach = IOFile.read(fS);
            System.out.println(listSach);
        } else {
            this.listSach = new ArrayList<>();
        }
    }

    private void loadBD() {
        File f = new File(fBD);
        if (f.exists()) {
            this.listBD = IOFile.read(fBD);
            System.out.println(listBD);
        } else {
            this.listBD = new ArrayList<>();
        }
    }

    private void showSach(List<Sach> list) {
        tmSach.setRowCount(0);
        for (Sach i : list) {
            tmSach.addRow(i.toObject());
        }
    }

    private void showBD(List<BanDoc> list) {
        tmBD.setRowCount(0);
        for (BanDoc i : list) {
            tmBD.addRow(i.toObject());
        }
    }

